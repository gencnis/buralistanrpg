/*
    This document is used in all of the pages, highlights the navigation bar items when the mouse is on them
*/

function highlightNav(x) {
    x.style.color = "#B1B1B1";
}

function normalNav(x) {
    x.style.color = "#F0F1F2"; //degisti
}

function highlightMenu(x) {
    x.style.background = "#B1B1B1"; //degisti
}

function normalMenu(x) {
    x.style.background = "#F0F1F2;"; //degisti
}